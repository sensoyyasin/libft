#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*src;
	char	*dest;

	i = 0;
	src = (char *)s1;
	dest = (char *)malloc(ft_strlen(src) + 1);
	if (dest == 0)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
